package com.corza.newapplicacionc01.models;

public class address_model
{
	  String calle_numero = "";
	  String colonia_estado = "";
	  String codigo_postal = "";

	  public String getCalle_numero() {
		    return calle_numero;
	  }
	  public void setCalle_numero(String calle_numero) {
		    this.calle_numero = calle_numero;
	  }

	  public String getColonia_estado() {
		    return colonia_estado;
	  }
	  public void setColonia_estado(String colonia_estado) {
		    this.colonia_estado = colonia_estado;
	  }

	  public String getCodigo_postal() {
		    return codigo_postal;
	  }
	  public void setCodigo_postal(String codigo_postal) {
		    this.codigo_postal = codigo_postal;
	  }

}
